package basicdemo;
// for loop - it should be used to execute a loop fixed no of times
public class ForDemo {

	public static void main(String[] args) {
		
		int i,j;
		System.out.println("**** Display 1-25 ****");
		
		for(i=1;i<=25;i++) {
			
			System.out.println(i);
		}
		System.out.println("**** REVERSE LOOP 55-30 ****");
		for(j=55;j>=30;j--) {
			System.out.println(j);
		}

	}

}
